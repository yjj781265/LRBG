class KeyStone {
    private int KeyStone;
    private String KeyStoneName;

    public KeyStone(int KeyStone , String KeyStoneName){
        this.KeyStone = KeyStone;
        this.KeyStoneName = KeyStoneName;
    }
    public  int getKeyStone(){
        return KeyStone;
    }
    public String getKeyStoneName(){
        return KeyStoneName;
    }

}
